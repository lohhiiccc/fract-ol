#include "libft.h"

void	*ft_memchr(const void *b, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((const char *)b)[i] == (char)c)
			return ((void *)b + i);
		i++;
	}
	return (NULL);
}
